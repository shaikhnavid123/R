# Operators in R :

# Arithmetic : +, -, *, /, %%, %/%, ^  
# Relational : <, >, ==, <=, >=, !=
# Logical : &, |, !, &&, ||
# Assignment : =, <-, ->, <<-, ->>

# Arithmetic Operators :

a<-7.5
b<-2

print(a+b) # Addition
print(a-b) # Substraction
print(a/b) # Division
print(a*b) # Multiplication
print(a%%b) # Reminder
print(a%/%b) # Quotient 
print(a^b) # Power/Exponent

# Arithm(etic Operators with Vectors :

c1<-c(8,9,6)
c2<-c(2,4,5)

print(c1+c2) # Addition
print(c1-c2) # Substraction
print(c1/c2) # Division
print(c1*c2) # Multiplication
print(c1%%c2) # Reminder
print(c1%/%c2) # Quotient 
print(c1^c2) # Power/Exponent

# Relation Operators :

a<-7.5
